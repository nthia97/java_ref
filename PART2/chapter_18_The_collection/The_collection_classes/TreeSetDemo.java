//Demonstrate TreeSet
import java.util.*;

class TreeSetDemo {
	public static void main(String[] args) {
		//Create a TreeSet
		TreeSet<String> ts=new TreeSet<String>();

		//Add elements to the TreeSet
		ts.add("C");
		ts.add("A");
		ts.add("B");
		ts.add("D");
		ts.add("E");
		ts.add("F");

		System.out.println(ts);

	}
	
}